package com.example.max.medievaltd;

public class coordinates {

    private int x;
    private int y;
    public coordinates(int X,int Y)
    {
        x=X;
        y=Y;
    }
    public coordinates()
    {

    }
    public void setX(int X)
    {
        x=X;
    }
    public void setY(int Y)
    {
        y=Y;
    }
    public int getX()
    {
        return x;
    }
    public int getY()
    {
        return y;
    }

}
